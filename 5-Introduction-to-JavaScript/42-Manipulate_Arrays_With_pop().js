// Learned to use the .pop() function to pop off the last value of the array and returns that value so that you can store it in a variable if you so choose

// Example
var ourArray = [1,2,3];
var removedFromOurArray = ourArray.pop();
// removedFromOurArray now equals 3, and ourArray now equals [1,2]

