// Learned to use the .shift() function to remove the first value stored in an array and returns that value so that you can store it in a variable

// Example
var ourArray = ["Stimpson", "J", ["cat"]];
var removedFromOurArray = ourArray.shift();
// removedFromOurArray now equals "Stimpson" and ourArray now equals ["J", ["cat"]].

